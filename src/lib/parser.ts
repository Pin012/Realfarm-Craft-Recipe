import { Recipe, Ingredient } from '@/types';
import Papa from 'papaparse';

export const parseExcelData = (csvText: string): Recipe[] => {
  // Parse CSV/TSV data
  const results = Papa.parse(csvText, {
    header: false,
    skipEmptyLines: true,
  });

  const rows = results.data as string[][];
  const recipes: Recipe[] = [];

  let currentMainCategory = '';
  let currentSubCategory = '';

  for (let i = 0; i < rows.length; i++) {
    const row = rows[i];
    
    // Column A: Main Category
    // Column B: Sub Category
    // Column C: Recipe Name
    // Column D+: Ingredients

    const colA = row[0]?.trim();
    const colB = row[1]?.trim();
    const colC = row[2]?.trim();

    if (colA) currentMainCategory = colA;
    if (colB) currentSubCategory = colB;

    // Skip if no recipe name, or if it looks like a header row
    // If colB is '分類', it's a header row.
    // If colC is empty, it's likely a category definition row without a recipe.
    if (!colC || colB === '分類' || colC === '分類') continue;

    const ingredients: Ingredient[] = [];

    // Iterate from Column D onwards
    for (let j = 3; j < row.length; j++) {
      const cell = row[j]?.trim();
      if (!cell) continue;

      let name = cell;
      let amount = 1;

      // Try to split by 'x' or '*' or 'X' or '×'
      // Matches: Name followed by separator and number
      const splitMatch = cell.match(/^(.+?)[xX*×](\d+)$/);
      
      if (splitMatch) {
        name = splitMatch[1].trim();
        amount = parseInt(splitMatch[2], 10);
      } else {
        // Fallback: check if it ends with a number
        const numberMatch = cell.match(/^(.+?)(\d+)$/);
        if (numberMatch) {
          name = numberMatch[1].trim();
          amount = parseInt(numberMatch[2], 10);
        }
      }

      ingredients.push({ name, amount });
    }

    recipes.push({
      id: `${currentMainCategory}-${currentSubCategory}-${colC}-${i}`,
      mainCategory: currentMainCategory,
      subCategory: currentSubCategory,
      name: colC,
      ingredients,
    });
  }

  return recipes;
};

